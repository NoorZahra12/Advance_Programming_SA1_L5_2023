#making a variable called count and assigning a number
count=1
#using while loop which will keep running until
while count<=10:
    #if count is equal to 6 meaning 5 because you count from 0
    if count==6:
        #then the loop will break/stop
        break
    #otherwise it will print the number and increment by 1
    else:
        #prints current count number
        print(count)
        #increases number by 1
        count += 1